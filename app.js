const langButtons = document.querySelectorAll(".lang-btn");
let langSwapping = false;

function setTexts(lang) {
  const dict = I18N[lang];
  document.documentElement.setAttribute("lang", lang);
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] != null) el.textContent = dict[key];
  });
  langButtons.forEach((b) => b.classList.toggle("is-active", b.dataset.lang === lang));
  try { localStorage.setItem("alatau_lang", lang); } catch (e) {}
}

function applyLang(lang, animate) {
  if (!I18N[lang]) return;
  const rm = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (!animate || rm) { setTexts(lang); return; }
  if (langSwapping) return;
  langSwapping = true;
  document.body.classList.add("lang-fading");
  setTimeout(() => {
    setTexts(lang);
    if (window.ScrollTrigger) window.ScrollTrigger.refresh();
    document.body.classList.remove("lang-fading");
    langSwapping = false;
  }, 360);
}

langButtons.forEach((btn) => btn.addEventListener("click", () => applyLang(btn.dataset.lang, true)));
(function initLang() {
  let saved; try { saved = localStorage.getItem("alatau_lang"); } catch (e) {}
  const sys = (navigator.language || "ru").slice(0, 2);
  const map = { ko: "kr", zh: "cn", en: "en", ru: "ru" };
  applyLang(saved || map[sys] || "ru", false);
})();

