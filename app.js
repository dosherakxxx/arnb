(function preloader() {
  const el = document.getElementById("preloader");
  const cnt = document.getElementById("preCount");
  if (!el) return;
  let v = 0;
  const tick = setInterval(() => {
    v += Math.floor(Math.random() * 8) + 3;
    if (v >= 100) { v = 100; clearInterval(tick); setTimeout(() => el.classList.add("done"), 350); }
    cnt.textContent = String(v).padStart(2, "0");
  }, 90);
})();

(function cursor() {
  const dot = document.getElementById("cursor");
  if (!dot || window.matchMedia("(hover: none)").matches) return;
  let x = innerWidth / 2, y = innerHeight / 2, cx = x, cy = y;
  addEventListener("mousemove", (e) => { x = e.clientX; y = e.clientY; dot.style.opacity = 1; });
  (function loop() {
    cx += (x - cx) * 0.18; cy += (y - cy) * 0.18;
    dot.style.transform = `translate(${cx}px, ${cy}px) translate(-50%,-50%)`;
    requestAnimationFrame(loop);
  })();
  document.querySelectorAll("a, button, .district, .f-link").forEach((el) => {
    el.addEventListener("mouseenter", () => dot.classList.add("is-hover"));
    el.addEventListener("mouseleave", () => dot.classList.remove("is-hover"));
  });
})();

